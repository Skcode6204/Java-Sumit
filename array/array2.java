//initialisation of array using loops

import java.util.*;
public class array2 {
    public static void main(String args[]) {
        int marks[] = new int[3];
        marks[0] = 87;
        marks[1] = 65;
        marks[2] = 97;

        for(int i=0; i<3; i++) {
            System.out.println(marks[i]);
        }

    }
}