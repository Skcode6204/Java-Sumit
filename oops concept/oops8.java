// java No-args Constructor.


public class oops8{
    int num1;
    int num2;

    //creating no - args constructor.
    public oops8(){
        num1 = -1;
        num2 = 2;

    }

    public static void main(String[] args){
        oops8 n = new oops8();

        //printing the values.
        System.out.println("num1 :"+n.num1);
        System.out.println("num2 :"+n.num2);
        

    }
}