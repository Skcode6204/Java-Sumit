//print table of any number given by user using java codes

import java.util.*;
public class loops4 {
    public static void main(String[] args) {
        //input
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i;
        for(i=1; i<11; i++) {
            System.out.println(n*i);

        }
        
    }
}